#include "libft.h"

int	ft_putstr_fd(char *s, int fd)
{
	int i;

	i = 0;
	if (!s)
	{
		write(fd, "(null)", 6);
		return (6);
	}
	while (*s)
	{
		write(fd, s, 1);
		s++;
		i++;
	}
	return (i);
}

